CREATE TABLE [dbo].[Car]
(
	[Id] INT IDENTITY(1,1) PRIMARY KEY, 
    [Owner] INT FOREIGN KEY REFERENCES CUser(Id) NOT NULL, 
    [Mechanic] INT FOREIGN KEY REFERENCES CUser(Id) NOT NULL, 
    [Make] INT FOREIGN KEY REFERENCES Make(Id) NOT NULL, 
    [Model] INT FOREIGN KEY REFERENCES Model(Id) NOT NULL, 
    [Trim] INT FOREIGN KEY REFERENCES CTrim(Id) NOT NULL, 
    [Fuel] INT FOREIGN KEY REFERENCES Fuel(Id) NOT NULL, 
    [CylinderConfig] INT FOREIGN KEY REFERENCES CylinderConfig(Id) NOT NULL, 
    [Aspiration] INT FOREIGN KEY REFERENCES Aspiration(Id) NOT NULL, 
    [Drivetrain] INT FOREIGN KEY REFERENCES Drivetrain(Id) NOT NULL, 
    [Transmission] INT FOREIGN KEY REFERENCES Transmission(Id) NOT NULL, 
    [EngineTech] INT FOREIGN KEY REFERENCES EngineTech(Id) NOT NULL, 
    [Body] INT FOREIGN KEY REFERENCES Body(Id) NOT NULL, 
    [Mileage] INT NOT NULL, 
    [Condition] INT FOREIGN KEY REFERENCES Condition(Id) NOT NULL, 
    [Year] INT NOT NULL, 
    [Doors] INT NOT NULL, 
    [Vin] VARCHAR(200) NOT NULL, 
    [EngineCode] VARCHAR(200) NOT NULL, 
    [CylinderCount] INT NOT NULL, 
    [TransmissionGears] INT NOT NULL, 
    [Wheelbase] FLOAT NOT NULL, 
    [Length] FLOAT NOT NULL, 
    [Width] FLOAT NOT NULL, 
    [Height] FLOAT NOT NULL, 
    [Color] VARCHAR(200) NOT NULL, 
    [MaxHP] INT NOT NULL, 
    [MaxTorque] INT NOT NULL, 
    [PeakRPM] INT NOT NULL, 
    [Redline] INT NOT NULL, 
    [TireSize] VARCHAR(200) NOT NULL, 
    [ZeroToSixty] FLOAT NOT NULL, 
    [ZeroToHundred] FLOAT NOT NULL, 
    [QuarterMile] FLOAT NOT NULL, 
    [Added] DATETIME NOT NULL, 
    [Edited] DATETIME NOT NULL, 
    [BodyCondition] VARCHAR(200) NOT NULL, 
    [EngineCondition] VARCHAR(200) NOT NULL, 
    [OilCondition] VARCHAR(200) NOT NULL, 
    [Cams] VARCHAR(50) NOT NULL
)
